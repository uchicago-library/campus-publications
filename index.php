<h2><a href="/search/?browse-category=student">Student Publications</a></h2>

<p>Independent student organizations at the University of Chicago have issued many
types of publications: yearbooks, newspapers, humor magazines, literary
journals, and newsletters, among others. Some student titles have had lengthier
